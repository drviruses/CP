void virus(){
          string seq;
          cin>>seq;
          ll len = (ll)seq.size();
          stack<char> st;
          st.push(seq[0]);
          for(auto i=1; i<len; i++){
              if(st.size() != 0 and st.top() != seq[i]  and tolower(st.top()) == tolower(seq[i])) st.pop();
              else st.push(seq[i]);
          }
          string ans = "";
          while(!st.empty()){
              ans += st.top();
              st.pop();
          }
          if(ans.size() != 0) reverse(all(ans));
          cout<<ans<<endl;
}


